    public frmEstacionamiento() {
        initComponents();      
        MostrarTabla();
        String Titulos[] = {"ID PARKING", "UBICACION", "ID NIVEL"};
        DefaultTableModel df = new DefaultTableModel(null, Titulos);
        ClsJoinParkingNivel ClsJoinPN = new ClsJoinParkingNivel();
        var MostrarJoinParkingNivel = ClsJoinPN.MostrarJoinParkingNivel();
        String Filas[] = new String[3];

        for (var iterador : MostrarJoinParkingNivel) {

            Filas[0] = String.valueOf(iterador.getId_parking());
            Filas[1] = iterador.getUbicacion();
            Filas[2] = String.valueOf(iterador.getId_nivel());
            df.addRow(Filas);
        }

        tbParkinsYEstacionamientos.setModel(df);

    }
    
    void MostrarTabla ()
    {
    
        String TITULOS [] = {"ID ESTACIONAMIENTO","ID NIVEL","ID PARQUEO","ESTADO"};
        DefaultTableModel ModeloTabla = new DefaultTableModel(null, TITULOS);
        ClsEstacionamiento claseEstacionamiento = new ClsEstacionamiento();
        ArrayList <Estacionamiento> Estacionamientos =  claseEstacionamiento.MostrarEstacionamientos();
        String filas [] = new String [5];
        for (var IterarDatos : Estacionamientos){
        
        filas[0] = String.valueOf(IterarDatos.getId_estacionamiento());
        filas[1] = String.valueOf(IterarDatos.getId_nivel());
        filas[2] = String.valueOf(IterarDatos.getId_parqueo());
        filas[3] = String.valueOf(IterarDatos.getEstado());
        
        
        ModeloTabla.addRow(filas);
            
        }
    
        tbEstacionamientos.setModel(ModeloTabla);
    }
    
   
    
