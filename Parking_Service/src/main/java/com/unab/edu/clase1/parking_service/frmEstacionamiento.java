    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtId.getText().equals("")) {
            if (txtNivel.getText() != "" && txtParqueo.getText() != "") {

                ClsEstacionamiento Estacionamientos = new ClsEstacionamiento();
                Estacionamiento Estacionamiento = new Estacionamiento();
                Estacionamiento.setId_nivel(Integer.parseInt(txtNivel.getText()));
                Estacionamiento.setId_parqueo(Integer.parseInt(txtParqueo.getText()));
                Estacionamientos.GuardarEstacionamiento(Estacionamiento);
                MostrarTabla();
                Limpiar();
                
            } else {

                JOptionPane.showMessageDialog(null, "Hay campos Vacios");
            }
        }
        else if (txtId.getText() != ""){
            
            if (txtNivel.getText() != "" && txtParqueo.getText() != "") {
           
                ClsEstacionamiento Estacionamientos = new ClsEstacionamiento();
                Estacionamiento Estacionamiento = new Estacionamiento();
                Estacionamiento.setId_estacionamiento(Integer.parseInt(txtId.getText()));
                Estacionamiento.setId_nivel(Integer.parseInt(txtNivel.getText()));
                Estacionamiento.setId_parqueo(Integer.parseInt(txtParqueo.getText()));
                Estacionamientos.ActualiarEstacionamiento(Estacionamiento);
                MostrarTabla();
                Limpiar();
           }
        else{
            
            JOptionPane.showMessageDialog(null, "Hay campos Vacios");
        }
            
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtNivel.getText() != "" && txtParqueo.getText() != "" && txtId.getText() != "") {
                
            ClsEstacionamiento Estacionamientos = new ClsEstacionamiento();
            Estacionamiento Estacionamiento = new Estacionamiento();
            Estacionamiento.setId_estacionamiento(Integer.parseInt(txtId.getText()));
            Estacionamientos.EliminarEstacionamiento(Estacionamiento);
            MostrarTabla();
            Limpiar();
        }
        else{
            
            JOptionPane.showMessageDialog(null, "Hay campos Vacios");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

